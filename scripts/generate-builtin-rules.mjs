import { readdir, readFile, writeFile } from "node:fs/promises";
import { join, relative } from "node:path";

const projectRoot = process.cwd();
const rulesRoot = join(projectRoot, "src", "rules");
const outputPath = join(projectRoot, "src", "core", "builtin-rules.generated.ts");

async function walkRules(dir) {
  const entries = (await readdir(dir, { withFileTypes: true }))
    .sort((left, right) => left.name.localeCompare(right.name));
  const files = [];

  for (const entry of entries) {
    const fullPath = join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...await walkRules(fullPath));
      continue;
    }
    if (!entry.isFile()) {
      continue;
    }
    if (!entry.name.endsWith(".json") || entry.name.endsWith(".schema.json") || entry.name.endsWith(".fixture.json")) {
      continue;
    }
    files.push(fullPath);
  }

  return files;
}

function buildImportPath(filePath) {
  return relative(join(projectRoot, "src", "core"), filePath).replaceAll("\\", "/");
}

function buildOutput(files) {
  const lines = [
    "// generated by scripts/generate-builtin-rules.mjs",
    'import type { JsonRule } from "../types.js";',
    "",
    ...files.map((filePath, index) => `import rule${index} from ${JSON.stringify(buildImportPath(filePath))} with { type: \"json\" };`),
    "",
    "export const BUNDLED_BUILTIN_RULES: JsonRule[] = [",
    ...files.map((filePath, index) => `  rule${index} as JsonRule, // ${relative(join(projectRoot, "src"), filePath).replaceAll("\\", "/")}`),
    "];",
    "",
  ];
  return `${lines.join("\n")}`;
}

const files = await walkRules(rulesRoot);
const nextContent = buildOutput(files);
const previousContent = await readFile(outputPath, "utf8").catch(() => "");

if (previousContent !== nextContent) {
  await writeFile(outputPath, nextContent, "utf8");
}
